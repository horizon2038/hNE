#define IO_MAPPING_TABLE_HPP

#include <core/common/common.hpp>

namespace core
{
    struct io_mapping_entry
    {
        address _address_base;
        address _address_limit;
    };

    // nes memory_map
    //
    // +--------------------------------+
    // | address |   size |       about |
    // +--------------------------------+
    // |  0x0000 | 0x0800 |        wram |
    // +--------------------------------+
    // |  0x0800 | 0x0800 | wram_mirror |
    // +--------------------------------+
    // |  0x2000 | 0x0008 |         ppu |
    // +--------------------------------+
    // |  0x2008 | 0x0008 |  ppu_mirror |
    // +--------------------------------+
    // |  0x4000 | 0x0018 |    i/o regs |
    // +--------------------------------+
    // |  0x4018 | 0x0020 |   test mode |
    // +--------------------------------+
    // |  0x4020 | 0x1fe0 |     ext-rom |
    // +--------------------------------+
    // |  0x6000 | 0x2000 |     ext-ram |
    // +--------------------------------+
    // |  0x8000 | 0x4000 | program-rom |
    // +--------------------------------+
    // |  0xc000 | 0x4000 | program-rom |
    // +--------------------------------+

    inline static io_mapping_entry io_mapping_table[10] = {
        { 0x0000, 0x0800 }, { 0x0800, 0x0800 }, { 0x2000, 0x0008 },
        { 0x2008, 0x0008 }, { 0x4000, 0x0018 }, { 0x4018, 0x0020 },
        { 0x4020, 0x1fe0 }, { 0x6000, 0x2000 }, { 0x8000, 0x4000 },
        { 0xc000, 0x4000 },
    };
}

